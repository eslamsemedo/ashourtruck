const dictionaries = { en, ar }

export function useT() {
  const code = useSelector((s: RootState) => s.lang.code)
  const dict = dictionaries[code] ?? dictionaries.en
  const t = (key: keyof typeof en) => dict[key] || String(key)
  return { t, code }
}

export type TranslationKey = keyof typeof en

export function Trans({ k }: { k: TranslationKey }) {
  const { t } = useT()
  return <>{t(k)}</>
}


